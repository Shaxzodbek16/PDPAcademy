import 'dart:math';

void main (){
  num num1=10, num2=20;
  print("$num1 + $num2 = ${num1+num2}");
  print("$num1 - $num2 = ${num1-num2}");
  print("$num1 * $num2 = ${num1*num2}");
  print("${num1}^2 = ${pow(num1, 2)}");
  print("${num2}^2 = ${pow(num2, 2)}");
}